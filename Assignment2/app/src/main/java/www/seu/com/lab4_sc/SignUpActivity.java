    /**
     * Represents an asynchronous login/registration task used to authenticate
     * the user.
     */
    public class UserLoginTask extends AsyncTask<Void, Void, String> {

        private final String mEmail;
        private final String mPassword,gender;
        private  final Context c;
        UserLoginTask(String email, String password,String sel_gen,Context c) {
            this.c=c.getApplicationContext();
            mEmail = email;
            mPassword = password;
            gender=sel_gen;
        }

        @Override
        protected String doInBackground(Void... params) {
            // TODO: attempt authentication against a network service.
            // TODO: prepare http post parameter
            HashMap<String, String> paras = new HashMap<>();
            paras.put("username", mEmail);
            paras.put("password", mPassword);
            paras.put("type", "signup");
            paras.put("gender", gender);
            // Calling post function from HttpCall class
            JSONObject json = new HttpCall().postForJSON(Login, paras);
            try {
                if (json==null)
                    return  "error-j";
                int success= json.getInt("success");
                if (success==-2)
                    return "error";
                if (success==-1)
                    return json.getString("errors");

//                SharedPreferences.Editor ed=getSharedPreferences(MainActivity.MY_PREFS_NAME,MODE_PRIVATE).edit();
//                ed.putString("username",mEmail).commit();
                return "logincomplete";

            } catch (JSONException e) {
                e.printStackTrace();
                return "error";

            }

        }

        @Override
        protected void onPostExecute(final String success) {
            mAuthTask = null;
            // Hide Progressbar, show result
            showProgress(false);
            switch (success){
                case "error-j":
                case "error":
                    Toast.makeText(getApplicationContext(), "Error Occurred! Try Again", Toast.LENGTH_SHORT).show();
                    break;

                case "invalid":
                    Toast.makeText(getApplicationContext(),"Email or Password Invalid!",Toast.LENGTH_LONG).show();

                    break;
                case "logincomplete":
                    int t=getIntent().getIntExtra("launcher",-1);
                    if(t==-1) {
                        Intent i = new Intent(SignUpActivity.this, LoginActivity.class);
                        i.setFlags(Intent.FLAG_ACTIVITY_CLEAR_TOP);
                        startActivity(i);
                    }
                    setResult(99);
                    finish();
                    break;
                default:
                    super.onPostExecute(success);

            }
        }

        @Override
        protected void onCancelled() {
            mAuthTask = null;
            showProgress(false);
        }
    }
